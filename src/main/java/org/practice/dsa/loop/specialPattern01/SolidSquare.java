package org.practice.dsa.loop.specialPattern01;

import java.util.Scanner;

public class SolidSquare {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);

        System.out.println("Enter Number : ");
        int a= sc.nextInt();
        for(int i=1 ;i<=a;i++){
            for (int j=1 ;j<=a ;j++){
                System.out.println();

            }
        }
    }
}
